import java.util.List;

public class SketchData {

    private int sketch_num;
    private int index;

    public SketchData(int sketch_num, int index) {
        this.sketch_num = sketch_num;
        this.index = index;
    }

    public int getSketch_num() {
        return sketch_num;
    }

    public int getIndex() {
        return index;
    }
}
